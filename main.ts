sprites.onOverlap(SpriteKind.Player, SpriteKind.Projectile, function (sprite, otherSprite) {
    if (canCollide) {
        if (sprite.vx != 0 || sprite.vy != 0) {
            info.player2.changeLifeBy(-1)
        } else {
            info.player1.changeLifeBy(-1)
        }
        canCollide = false
    }
})
